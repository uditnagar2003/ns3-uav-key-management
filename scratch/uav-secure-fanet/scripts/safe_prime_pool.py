def get_unique_batch(n=10):
    if n > len(SAFE_PRIME_POOL):
        raise ValueError(f"Requested {n} pairs but pool only has {len(SAFE_PRIME_POOL)}")
    return random.sample(SAFE_PRIME_POOL, n)

def get_sequential_batch(n=10, offset=0):
    if offset + n > len(SAFE_PRIME_POOL):
        raise ValueError(f"Not enough primes: need {offset+n}, have {len(SAFE_PRIME_POOL)}")
    return SAFE_PRIME_POOL[offset:offset + n]
