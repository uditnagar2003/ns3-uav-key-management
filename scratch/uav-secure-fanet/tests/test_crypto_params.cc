// Test 6: Lookup functions
// ===========================================================================
bool test_lookup_functions() {
    const std::string path = "json/crypto_params.json";
    std::ifstream f(path);
    if (!f.good()) {
        std::cout << "  SKIP: json/crypto_params.json not found\n";
        return true;
    }
    f.close();

    auto params = CryptoParamsLoader::LoadFromFile(path);

    // GetCluster
    const auto* c0 = params.GetCluster(0);
    const auto* c1 = params.GetCluster(1);
    const auto* c2 = params.GetCluster(2);
    ASSERT_TRUE(c0 != nullptr);
    ASSERT_TRUE(c1 != nullptr);
    ASSERT_TRUE(c2 != nullptr);
    ASSERT_TRUE(params.GetCluster(99) == nullptr);

    // GetSlaveKey within cluster
    const auto* sk0 = c0->GetSlaveKey(0);
    const auto* sk5 = c0->GetSlaveKey(5);
    ASSERT_TRUE(sk0 != nullptr);
    ASSERT_TRUE(sk5 != nullptr);
    ASSERT_TRUE(c0->GetSlaveKey(99) == nullptr);

    // GetSlaveKeyByUavId (global)
    // UAV 0-5 in cluster 0, 6-11 in cluster 1, 12-17 in cluster 2
    const auto* uav0  = params.GetSlaveKeyByUavId(0);
    const auto* uav6  = params.GetSlaveKeyByUavId(6);
    const auto* uav17 = params.GetSlaveKeyByUavId(17);
    ASSERT_TRUE(uav0  != nullptr);
    ASSERT_TRUE(uav6  != nullptr);
    ASSERT_TRUE(uav17 != nullptr);
    ASSERT_TRUE(params.GetSlaveKeyByUavId(99) == nullptr);

    ASSERT_EQ(uav0->uav_id,   0u);
    ASSERT_EQ(uav6->uav_id,   6u);
    ASSERT_EQ(uav17->uav_id, 17u);

    std::cout << "  GetCluster(0,1,2): PASS\n";
    std::cout << "  GetSlaveKey(0,5): PASS\n";
    std::cout << "  GetSlaveKeyByUavId(0,6,17): PASS\n";
    std::cout << "  Not-found returns nullptr: PASS\n";
    return true;
}

// ===========================================================================
// Test 7: TEK hex to bytes
// ===========================================================================
bool test_tek_bytes() {
    const std::string path = "json/crypto_params.json";
    std::ifstream f(path);
    if (!f.good()) {
        std::cout << "  SKIP: json/crypto_params.json not found\n";
        return true;
    }
    f.close();

    auto params = CryptoParamsLoader::LoadFromFile(path);

    for (utils::u32 c = 0; c < params.num_clusters; ++c) {
        const auto* cluster = params.GetCluster(c);

        // TEK must be 32 bytes
        ASSERT_EQ(cluster->tek.size(), 32u);

        // TEK hex must match TEK bytes
        std::string reconstructed;
        for (utils::u8 b : cluster->tek) {
            char buf[3];
            std::snprintf(buf, sizeof(buf), "%02x", b);
            reconstructed += buf;
        }
        ASSERT_EQ(reconstructed, cluster->tek_hex);

        std::cout << "  Cluster " << c
                  << " TEK: " << cluster->tek_hex.substr(0, 16)
                  << "...\n";
    }
    return true;
}

