// Test 1: Load from file
// ===========================================================================
bool test_load_from_file() {
    const std::string path = "json/crypto_params.json";

    // Check file exists first
    std::ifstream f(path);
    if (!f.good()) {
        std::cerr << "  SKIP: " << path << " not found.\n";
        std::cerr << "  Run: python3 scripts/gen_crypto.py first\n";
        return true;  // not a failure — just not generated yet
    }
    f.close();

    auto params = CryptoParamsLoader::LoadFromFile(path);

    ASSERT_EQ(params.scheme,           std::string("MKE-MGKM"));
    ASSERT_EQ(params.num_clusters,     3u);
    ASSERT_EQ(params.uavs_per_cluster, 6u);
    ASSERT_EQ(params.total_uavs,       18u);
    ASSERT_EQ(params.clusters.size(),  3u);

    std::cout << "  Scheme: " << params.scheme << "\n";
    std::cout << "  Clusters: " << params.num_clusters << "\n";
    std::cout << "  UAVs: " << params.total_uavs << "\n";
    return true;
}

// ===========================================================================
// Test 2: Cluster structure
// ===========================================================================
bool test_cluster_structure() {
    const std::string path = "json/crypto_params.json";
    std::ifstream f(path);
    if (!f.good()) {
        std::cout << "  SKIP: json/crypto_params.json not found\n";
        return true;
    }
    f.close();

    auto params = CryptoParamsLoader::LoadFromFile(path);

    for (utils::u32 c = 0; c < params.num_clusters; ++c) {
        const auto* cluster = params.GetCluster(c);
        ASSERT_TRUE(cluster != nullptr);
        ASSERT_EQ(cluster->cluster_id,    c);
        ASSERT_EQ(cluster->skdc_id,       c);
        ASSERT_EQ(cluster->num_uavs,      6u);
        ASSERT_EQ(cluster->slave_keys.size(), 6u);
        ASSERT_EQ(cluster->user_indices.size(), 6u);
        ASSERT_TRUE(cluster->eM   > BigInt(0));
        ASSERT_TRUE(cluster->MT_K > BigInt(0));
        ASSERT_TRUE(cluster->tek_int    > BigInt(0));
        ASSERT_EQ(cluster->tek_hex.size(), 64u);
        ASSERT_EQ(cluster->tek.size(),     32u);

        std::cout << "  Cluster " << c << ":\n";
        std::cout << "    eM   = "
                  << BigIntOps::ToDecString(cluster->eM).substr(0,30)
                  << "...\n";
        std::cout << "    MT_K = "
                  << BigIntOps::ToDecString(cluster->MT_K).substr(0,30)
                  << "...\n";
        std::cout << "    TEK  = "
                  << cluster->tek_hex.substr(0,20) << "...\n";
    }
    return true;
}

// ===========================================================================
// Test 3: Slave key structure
// ===========================================================================
bool test_slave_key_structure() {
    const std::string path = "json/crypto_params.json";
    std::ifstream f(path);
    if (!f.good()) {
        std::cout << "  SKIP: json/crypto_params.json not found\n";
        return true;
    }
    f.close();

    auto params = CryptoParamsLoader::LoadFromFile(path);

    for (utils::u32 c = 0; c < params.num_clusters; ++c) {
        const auto* cluster = params.GetCluster(c);
        ASSERT_TRUE(cluster != nullptr);

        for (const auto& sk : cluster->slave_keys) {
            // All fields must be positive
            ASSERT_TRUE(sk.e_i  > BigInt(0));
            ASSERT_TRUE(sk.d_i  > BigInt(0));
            ASSERT_TRUE(sk.n_i  > BigInt(0));
            ASSERT_TRUE(sk.x_i  > BigInt(0));
            ASSERT_TRUE(sk.y_i  > BigInt(0));
            ASSERT_TRUE(sk.Mi   > BigInt(0));
            ASSERT_TRUE(sk.Ni   > BigInt(0));
            ASSERT_TRUE(sk.xy_i > BigInt(0));
            ASSERT_TRUE(sk.p_i  > BigInt(0));
            ASSERT_TRUE(sk.q_i  > BigInt(0));

            // xy_i == x_i * y_i
            ASSERT_EQ(sk.xy_i, sk.x_i * sk.y_i);

            // e_i ≡ 1 mod 4
            ASSERT_EQ(BigIntOps::Mod(sk.e_i, BigInt(4)),
                      BigInt(1));
        }
    }

    std::cout << "  All slave keys: positive fields PASS\n";
    std::cout << "  xy_i == x_i * y_i: PASS\n";
    std::cout << "  e_i ≡ 1 mod 4: PASS\n";
    return true;
}

// ===========================================================================
// Test 4: Slave decryption property verification
// ===========================================================================
bool test_slave_decryption_property() {
    const std::string path = "json/crypto_params.json";
    std::ifstream f(path);
    if (!f.good()) {
        std::cout << "  SKIP: json/crypto_params.json not found\n";
        return true;
    }
    f.close();

    auto params = CryptoParamsLoader::LoadFromFile(path);

    int verified = 0;
    // Verify first slave in each cluster
    for (utils::u32 c = 0; c < params.num_clusters; ++c) {
        const auto* cluster = params.GetCluster(c);
        ASSERT_TRUE(!cluster->slave_keys.empty());

        const auto& sk = cluster->slave_keys[0];

        // d_i = e_i^(2*(x_i-1)*(y_i-1)-1) mod 4*x_i*y_i
        BigInt mod4xy  = 4 * sk.x_i * sk.y_i;
        BigInt exp_val = 2 * (sk.x_i - 1) * (sk.y_i - 1) - 1;
        BigInt expected = BigIntOps::ModPow(sk.e_i, exp_val, mod4xy);
        ASSERT_EQ(sk.d_i, expected);

        std::cout << "  Cluster " << c
                  << " UAV 0: d_i property VERIFIED\n";
        ++verified;
    }

    ASSERT_EQ(verified, 3);
    return true;
}

// ===========================================================================
// Test 5: MT_K slave decryption (main security property)
// pow(MT_K, d_i, n_i) == T mod n_i  for each UAV in cluster
// ===========================================================================
bool test_mtk_slave_decryption() {
    const std::string path = "json/crypto_params.json";
    std::ifstream f(path);
    if (!f.good()) {
        std::cout << "  SKIP: json/crypto_params.json not found\n";
        return true;
    }
    f.close();

    auto params = CryptoParamsLoader::LoadFromFile(path);

    int total_pass = 0;
    int total_fail = 0;

    for (utils::u32 c = 0; c < params.num_clusters; ++c) {
        const auto* cluster = params.GetCluster(c);
        ASSERT_TRUE(cluster != nullptr);

        const BigInt& MT_K = cluster->MT_K;
        const BigInt& T    = cluster->tek_int;

        for (const auto& sk : cluster->slave_keys) {
            // Core security property:
            // pow(MT_K, d_i, n_i) == T mod n_i
            BigInt recovered = BigIntOps::ModPow(
                MT_K, sk.d_i, sk.n_i);
            BigInt T_mod = BigIntOps::Mod(T, sk.n_i);

            bool ok = (recovered == T_mod);
            if (ok) {
                ++total_pass;
                std::cout << "  Cluster " << c
                          << " UAV " << sk.uav_id
                          << ": MT_K decrypt OK\n";
            } else {
                ++total_fail;
                std::cerr << "  Cluster " << c
                          << " UAV " << sk.uav_id
                          << ": MT_K decrypt FAILED\n";
            }
        }
    }

    std::cout << "  Total: " << total_pass << " pass, "
              << total_fail << " fail\n";

    ASSERT_EQ(total_fail, 0);
    ASSERT_EQ(total_pass, 18);
    return true;
}

